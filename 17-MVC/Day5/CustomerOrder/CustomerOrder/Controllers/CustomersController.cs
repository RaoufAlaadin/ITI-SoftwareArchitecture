using System;
using System.Collections.Generic;
using System.Data;
using System.Data.Entity;
using System.Linq;
using System.Net;
using System.Web;
using System.Web.Mvc;
using CustomerOrder.Data;
using CustomerOrder.Models;

namespace CustomerOrder.Controllers
{
    [RoutePrefix("CustomerData")]
    public class CustomersController : Controller
    {
        private CustomerOrderContext db = new CustomerOrderContext();

        // GET: Customers
        [Route("DataView")]
        public ActionResult Index()
        {
            return View(db.Customers.ToList());
        }

        [Route("DetailedView/{id:int:min(3)}")]

        // GET: Customers/Details/5
        public ActionResult Details(int? id)
        {
            if (id == null)
            {
                return new HttpStatusCodeResult(HttpStatusCode.BadRequest);
            }
            Customer customer = db.Customers.Find(id);
            if (customer == null)
            {
                return HttpNotFound();
            }
            return View(customer);
        }

        // GET: Customers/Create
        [Route("AddCustomer")]

        public ActionResult Create()
        {
            return View();
        }

        // POST: Customers/Create
        // To protect from overposting attacks, enable the specific properties you want to bind to, for 
        // more details see https://go.microsoft.com/fwlink/?LinkId=317598.
        [HttpPost]
        [ValidateAntiForgeryToken]
        [Route("AddCustomer")]

        public ActionResult Create([Bind(Include = "ID,Name,Gender,Email,PhoneNum")] Customer customer)
        {


            if (ModelState.IsValid)
            {
                db.Customers.Add(customer);
                db.SaveChanges();
                return RedirectToAction("Index");
            }

            return View(customer);
        }

        // GET: Customers/Edit/5

        [Route("EditView/{id:int:min(3)}")]

        public ActionResult Edit(int? id)
        {
            if (id == null)
            {
                return new HttpStatusCodeResult(HttpStatusCode.BadRequest);
            }
            Customer customer = db.Customers.Find(id);
            if (customer == null)
            {
                return HttpNotFound();
            }
            return View(customer);
        }

        // POST: Customers/Edit/5
        // To protect from overposting attacks, enable the specific properties you want to bind to, for 
        // more details see https://go.microsoft.com/fwlink/?LinkId=317598.
        [HttpPost]
        [ValidateAntiForgeryToken]

        [Route("EditView/{id:int:min(3)}")]

        public ActionResult Edit([Bind(Include = "ID,Name,Gender,Email,PhoneNum")] Customer customer)
        {
            /*Important before posting to db, to ensure no errors happened
             
             if ( emp.age < 18 ) { ModelState.AddModelError("Age","Age must be +18")}
            
            note: JQueryValidation also checks the ModelState, incase we didn't add DataAnonation
            for the client-side validations. 

             */
            if (ModelState.IsValid)
            {
                db.Entry(customer).State = EntityState.Modified;
                db.SaveChanges();
                return RedirectToAction("Index");
            }
            return View(customer);
        }

        // GET: Customers/Delete/5
        [Route("RemoveCustomer/{id:int:min(3)}")]

        public ActionResult Delete(int? id)
        {
            if (id == null)
            {
                return new HttpStatusCodeResult(HttpStatusCode.BadRequest);
            }
            Customer customer = db.Customers.Find(id);
            if (customer == null)
            {
                return HttpNotFound();
            }
            return View(customer);
        }

        [Route("RemoveCustomer/{id:int:min(3)}")]

        // POST: Customers/Delete/5
        [HttpPost, ActionName("Delete")]
        [ValidateAntiForgeryToken]
        public ActionResult DeleteConfirmed(int id)
        {
            Customer customer = db.Customers.Find(id);
            db.Customers.Remove(customer);
            db.SaveChanges();
            return RedirectToAction("Index");
        }

        protected override void Dispose(bool disposing)
        {
            if (disposing)
            {
                db.Dispose();
            }
            base.Dispose(disposing);
        }
    }
}
